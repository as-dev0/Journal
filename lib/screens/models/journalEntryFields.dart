class JournalEntryFields {

  String? title;
  String? body;
  String? rating;
  String? dateTime;

  JournalEntryFields();

  void setTitle(title){
    title = title;
  }

  void setBody(body){
    body = body;
  }

  void setRating(rating){
    rating = rating;
  }

  void setDateTime(dateTime){
    dateTime = dateTime;
  }

}